 public class Encapsulation {
        private int a=5, b=7;

        public int showResult(){
            return a+b;
        }

     public int getA() { //получить а
         return a;
     }

     public void setA(int a) {//установить а
         this.a = a;
     }

     public int getB() {
         return b;
     }

     public void setB(int b) {
         this.b = b;
     }
 }

